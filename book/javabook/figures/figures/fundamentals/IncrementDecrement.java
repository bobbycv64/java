public class IncrementDecrement {
  
  public static void main(String[] args) {
    
    int i = 0;
    System.out.println(i);	// i = 0
    System.out.println(i++);	// i = 0
    System.out.println(i++);  // i = 1
    System.out.println(++i);  // i = 3
    System.out.println(i--);  // i = 3
    System.out.println(--i);	// i = 1
  } 
}